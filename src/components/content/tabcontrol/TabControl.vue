<template>
  <div class="TabControl">
    <div v-for="(item,index) in titles" class="TabControl-item" :class="{active:index===currentIndex}" @click="itemclick(index)">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
  export default {
    name:'TabControl',
    props:{
      titles:{
        type:Array,
        default() {
          return []
        }
      }
    },
    data() {
      return{
        currentIndex: 0
      }
    },
    methods:{
      itemclick(index) {
       this.currentIndex = index
       this.$emit('tabclick',index)
      }
    }
  }
</script>

